function interpreter(tape) {
    let cells = [0];
    let selector = 0;
    let output = '';
    for (const command of tape) {
        switch (command) {
            case '>':
                selector++
                break;
            case '<':
                selector--
                break;
            case '+':
                if (selector >= 0 && selector < cells.length) {
                    cells[selector] = (cells[selector] + 1) % 256;
                }
                break;
            case '-':
                if (selector >= 0 && selector < cells.length) {
                    cells[selector] = (cells[selector] - 1 + 256) % 256;
                }
                break;
            case '*':
                output += String.fromCharCode(cells[selector] || 0);
                break;
            case '/':
                if (selector >= 0 && selector < cells.length) {
                    cells[selector] = 0;
                }
                break;
            case '!':
                cells.push(0);
                break;
            default:
                break;
        }
    }
    return output;
}

